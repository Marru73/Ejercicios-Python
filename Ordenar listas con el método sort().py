# Su sintaxis es: nombre_variable.sort()
# si no ponemos ningún argumento la ordenación será de manera ascendente.

numeros = [5, 3, 1, 2, 4]
numeros.sort()
print(numeros)

numeros = [5, 3, 1, 2, 4]
numeros.sort(reverse = True)
print(numeros)

vocales = ["a", "e", "i", "o", "u"]
vocales.sort()
print(vocales)

vocales.sort(reverse = True)
print(vocales)