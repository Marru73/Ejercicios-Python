# Su sintaxis es: nombre_lista.pop(posición)

vocales= ["a", "e", "i", "o", "u"]
vocales.pop() #Cuando no se pone ningún argumento, se eliminará el último de la lista.
print(vocales)

vocales= ["a", "e", "i", "o", "u"]
vocales.pop(2)
print(vocales)

vocales= ["a", "e", "i", "o", "u"]
vocales.pop(-2)
print(vocales)

