# Asignación con +=

mensaje = "Hola"
mensaje += " "
mensaje += "Marru"
print(mensaje)


#Concatenación con +

mensaje = "Hola"
espacio = " "
nombre = "Marru"
print (mensaje + espacio + nombre)

numero_uno = 4
numero_dos = 6
resultado = numero_uno + numero_dos
resultado = str(resultado)
print("El resultado de la suma es: " + resultado)



# Búsqueda con .Find

nombre = "Francisco José"
buscar = nombre.find("José")
print(buscar)

# Extracción con [ : ]

extraer = nombre[10:14]
print(extraer)

